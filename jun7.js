// You are given an array arr, if the length of the array is greater than 5 return “Hello” else return “World”

function arrayHelloWorld(arr){
  if(arr.length > 5){
    console.log("Hello")
  } else{
    console.log("World")
  }
  return arr;
}
arrayHelloWorld([1,2,3,4,5]);